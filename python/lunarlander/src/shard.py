class Shard:
	"""This class represents a shard of shrapnel produced by crashing your lander."""
	def __init__(self, xvelocity, yvelocity, avelocity, xpos, ypos, angle):
		self.xvelocity = xvelocity
		self.yvelocity = yvelocity
		self.avelocity = avelocity
		self.xpos = xpos
		self.ypos = ypos
		self.angle = angle


